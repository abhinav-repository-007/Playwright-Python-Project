# This program is about Dictionary operations
# Dictionary is DT, having {key : value}

value = {1 : 45, "a" : 2.3, 6 : "shivani", "cd" : "Hello World"}
print(value)            # {1: 45, 'a': 2.3, 6: 'shivani', 'cd': 'Hello World'}
print(type(value))      # <class 'dict'>
print(len(value))       # 4
print(value["a"])       # 2.3
print(value["cd"])      # Hello World
print(value[6])         # shivani
